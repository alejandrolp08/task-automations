function formatDuration(value: number | null) {
  if (!value) {
    return undefined;
  }

  return `${Math.floor(value / 60).toString().padStart(2, "0")}:${(value % 60)
    .toString()
    .padStart(2, "0")}`;
}

function buildListingSettings(row: SqlListingSettings): BatchListingSettings {
  return {
    location: row.location ?? "Toronto, Ontario",
    shippingType: row.shippingType === "Free" ? "Free" : "Flat",
    shippingCost: row.shippingCost ?? "1.99",
    returnsAcceptedOption:
      row.returnsAcceptedOption === "ReturnsNotAccepted" ? "ReturnsNotAccepted" : "ReturnsAccepted",
    returnsWithinOption: row.returnsWithinOption ?? "Days_30",
    refundOption: row.refundOption ?? "MoneyBack",
    shippingCostPaidByOption: row.shippingCostPaidByOption === "Seller" ? "Seller" : "Buyer",
    dispatchTimeMax: String(row.dispatchTimeMax ?? 3),
    cardConditionDescriptor: row.cardConditionDescriptor ?? "400010",
    descriptionTemplate:
      row.descriptionTemplate ??
      "{card_name} from {set_name}. Card number: {card_number}. Rarity: {rarity}. Generated by TCG Listing Platform draft export.",
  };
}

function buildCatalogFallbackImageUrl(externalSetId: string | null, cardNumber: string | null) {
  if (!externalSetId || !cardNumber) {
    return undefined;
  }

  const collectorNumber = cardNumber.split("/")[0];
  return `https://images.pokemontcg.io/${externalSetId}/${collectorNumber}_hires.png`;
}

function parseReviewMatchLabel(label: string) {
  const trimmed = label.trim();
  const lastSpaceIndex = trimmed.lastIndexOf(" ");
  const cardNumber = lastSpaceIndex > 0 ? trimmed.slice(lastSpaceIndex + 1).trim() : "";
  const name = lastSpaceIndex > 0 ? trimmed.slice(0, lastSpaceIndex).trim() : trimmed;

  if (!name || !cardNumber) {
    return null;
  }

  return { name, cardNumber };
}

async function getCatalogImageUrlsForReviewMatches(matchLabels: string[]) {
  const parsedMatches = matchLabels
    .map(parseReviewMatchLabel)
    .filter((value): value is NonNullable<typeof value> => Boolean(value));

  if (parsedMatches.length === 0) {
    return {};
  }

  const whereClauses = parsedMatches
    .map(
      ({ name, cardNumber }) =>
        `(CatalogCard.name = '${escapeSqliteString(name)}' AND CatalogCard.cardNumber = '${escapeSqliteString(cardNumber)}')`,
    )
    .join(" OR ");

  const rows = await queryRows<{
    name: string;
    cardNumber: string;
    externalSetId: string | null;
    imageLargeUrl: string | null;
    imageSmallUrl: string | null;
  }>(`
    SELECT
      CatalogCard.name,
      CatalogCard.cardNumber,
      CatalogSet.externalSetId,
      CatalogCard.imageLargeUrl,
      CatalogCard.imageSmallUrl
    FROM CatalogCard
    LEFT JOIN CatalogSet ON CatalogSet.id = CatalogCard.setId
    WHERE ${whereClauses};
  `);

  return Object.fromEntries(
    rows
      .map((row) => [
        `${row.name} ${row.cardNumber}`,
        row.imageLargeUrl ||
          row.imageSmallUrl ||
          buildCatalogFallbackImageUrl(row.externalSetId, row.cardNumber),
      ])
      .filter((entry): entry is [string, string] => Boolean(entry[1])),
  );
}

