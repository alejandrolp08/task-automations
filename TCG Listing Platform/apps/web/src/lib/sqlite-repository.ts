type CreateUploadInput = {
  batchId: string;
  fileName: string;
  storageKey: string;
  kind: "video" | "images";
  durationSeconds?: number;
  imageCount?: number;
};

export async function createSqliteUpload(input: CreateUploadInput): Promise<string> {
  const id = `upload-${crypto.randomUUID()}`;
  const fileName = escapeSqliteString(input.fileName);
  const storageKey = escapeSqliteString(input.storageKey);
  const kind = input.kind === "video" ? "VIDEO" : "IMAGES";
  const batchId = escapeSqliteString(input.batchId);

  await runStatement(`
    INSERT INTO Upload (
      id,
      batchId,
      kind,
      fileName,
      storageKey,
      status,
      durationSeconds,
      imageCount,
      uploadedAt
    ) VALUES (
      '${id}',
      '${batchId}',
      '${kind}',
      '${fileName}',
      '${storageKey}',
      'UPLOADED',
      ${typeof input.durationSeconds === "number" ? input.durationSeconds : "NULL"},
      ${typeof input.imageCount === "number" ? input.imageCount : "NULL"},
      CURRENT_TIMESTAMP
    );
  `);

  await runStatement(`
    UPDATE Batch
    SET
      intakeMode = '${kind}',
      status = 'PROCESSING',
      exportReady = 0,
      updatedAt = CURRENT_TIMESTAMP
    WHERE id = '${batchId}';
  `);

  return id;
}

