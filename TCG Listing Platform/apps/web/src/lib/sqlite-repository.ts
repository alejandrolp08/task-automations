type UpdateDetectionOverridesInput = {
  batchId: string;
  detectionId: string;
  titleOverride: string;
  priceOverride: string;
  quantityOverride: string;
  conditionOverride: string;
  excludeFromExport: boolean;
};

export async function updateDetectionOverrides(input: UpdateDetectionOverridesInput) {
  const safeBatchId = escapeSqliteString(input.batchId);
  const safeDetectionId = escapeSqliteString(input.detectionId);
  const quantityValue = Number.parseInt(input.quantityOverride, 10);

  await runStatement(`
    UPDATE Detection
    SET
      titleOverride = ${input.titleOverride ? `'${escapeSqliteString(input.titleOverride)}'` : "NULL"},
      priceOverride = ${input.priceOverride ? `'${escapeSqliteString(input.priceOverride)}'` : "NULL"},
      quantityOverride = ${Number.isFinite(quantityValue) && quantityValue > 0 ? quantityValue : "NULL"},
      conditionOverride = ${input.conditionOverride ? `'${escapeSqliteString(input.conditionOverride)}'` : "NULL"},
      excludeFromExport = ${input.excludeFromExport ? 1 : 0},
      updatedAt = CURRENT_TIMESTAMP
    WHERE id = '${safeDetectionId}' AND batchId = '${safeBatchId}';
  `);

  await recalculateBatchState(safeBatchId);
}
