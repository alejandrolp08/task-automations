function resolveDatabasePath() {
  const cwd = process.cwd();
  const candidates = [
    path.join(cwd, "prisma", "dev.db"),
    path.resolve(cwd, "..", "..", "prisma", "dev.db"),
  ];

  return candidates.find((candidate) => fs.existsSync(candidate)) ?? candidates[0];
}

const DATABASE_PATH = resolveDatabasePath();

async function queryRows<T>(sql: string): Promise<T[]> {
  const { stdout } = await execFile("sqlite3", ["-json", DATABASE_PATH, sql], {
    maxBuffer: 1024 * 1024 * 64,
  });
  const trimmed = stdout.trim();
  return trimmed ? (JSON.parse(trimmed) as T[]) : [];
}

async function runStatement(sql: string): Promise<void> {
  await execFile("sqlite3", [DATABASE_PATH, sql]);
}

function escapeSqliteString(value: string) {
  return value.replace(/'/g, "''");
}

